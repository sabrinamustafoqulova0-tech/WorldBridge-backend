async def seed_programs_4():
    async with AsyncSessionLocal() as session:
        added = 0
        skipped = 0
        for prog in PROGRAMS:
            existing = (
                await session.execute(
                    select(Program).where(Program.slug == prog["slug"])
                )
            ).scalar_one_or_none()
            if existing:
                print(f"  [skip] {prog['slug']} already exists")
                skipped += 1
                continue

            session.add(Program(**prog))
            print(f"  [add]  {prog['slug']}")
            added += 1

        await session.commit()
        print(f"\n  Done: {added} added, {skipped} skipped.")


if __name__ == "__main__":
    asyncio.run(seed_programs_4())
